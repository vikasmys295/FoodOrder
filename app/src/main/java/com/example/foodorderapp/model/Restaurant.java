package com.example.foodorderapp.model;

public class Restaurant {

    private String restaurant_name;
    private String restaurant_photo;
    private String restaurant_address;
    private String restaurant_rating;
    private String restaurant_avg_price;
    private String restaurant_dishes_type;
    private String restaurant_dishes;
    private String restaurant_cuisine_type;

    public String getRestaurant_name() {
        return restaurant_name;
    }

    public void setRestaurant_name(String restaurant_name) {
        this.restaurant_name = restaurant_name;
    }

    public String getRestaurant_photo() {
        return restaurant_photo;
    }

    public void setRestaurant_photo(String restaurant_photo) {
        this.restaurant_photo = restaurant_photo;
    }

    public String getRestaurant_address() {
        return restaurant_address;
    }

    public void setRestaurant_address(String restaurant_address) {
        this.restaurant_address = restaurant_address;
    }

    public String getRestaurant_rating() {
        return restaurant_rating;
    }

    public void setRestaurant_rating(String restaurant_rating) {
        this.restaurant_rating = restaurant_rating;
    }

    public String getRestaurant_avg_price() {
        return restaurant_avg_price;
    }

    public void setRestaurant_avg_price(String restaurant_avg_price) {
        this.restaurant_avg_price = restaurant_avg_price;
    }

    public String getRestaurant_dishes_type() {
        return restaurant_dishes_type;
    }

    public void setRestaurant_dishes_type(String restaurant_dishes_type) {
        this.restaurant_dishes_type = restaurant_dishes_type;
    }

    public String getRestaurant_dishes() {
        return restaurant_dishes;
    }

    public void setRestaurant_dishes(String restaurant_dishes) {
        this.restaurant_dishes = restaurant_dishes;
    }

    public String getRestaurant_cuisine_type() {
        return restaurant_cuisine_type;
    }

    public void setRestaurant_cuisine_type(String restaurant_cuisine_type) {
        this.restaurant_cuisine_type = restaurant_cuisine_type;
    }
}
